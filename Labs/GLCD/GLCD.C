void PAGE_COL_SEL( unsigned char pag,unsigned char col)
{
if(col<64)
{
GLCD_CS1_SET;
GLCD_CS2_CLR;
GLCD_CMD(0x40|col);
 GLCD_CMD(0xB8|pag);
}
else
{

GLCD_CS1_CLR;
GLCD_CS2_SET;
GLCD_CMD(0x40|(col-64));
 GLCD_CMD(0xB8|pag);
}
}

